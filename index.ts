// Definición de una función que calcula el área de un círculo
function areaCirculo(radio: number) {
    return Math.PI * radio * radio;
  }
  
  // Definición de una constante que representa el radio del círculo
  const radio = 5;
  
  // Llamada a la función para calcular el área
  const area = areaCirculo(radio);
  
  // Impresión del área
  console.log(area); // 78.53981633974483
  